	if ply:Team() == TEAM_NRG1 then 
		
		if ply:rg_GetLVL() < 3 then			-- 1
			return false, "[РГ Зарплата] Вам (Солдату) были перечислены 750руб!", 750
		elseif ply:rg_GetLVL() < 7 then		-- 2
			return false, "[РГ Зарплата] Вам (Сержанту) были перечислены 1500руб!", 1500
