			return false, "[РГ Зарплата] Вам (Прапорщику) были перечислены 2500руб!", 2500
		elseif ply:rg_GetLVL() < 13 then	-- 4
			return false, "[РГ Зарплата] Вам (Младшему Офицеру) были перечислены 4000руб!", 4000
		elseif ply:rg_GetLVL() < 16 then	-- 5
			return false, "[РГ Зарплата] Вам (Старшему Офицеру) были перечислены 8000руб!", 8000
