			return false, "[РГ Зарплата] Вам (Высшему Офицеру) были перечислены 15000руб!", 15000
		else								-- 7
			return false, "[РГ Зарплата] Вам (Маршалу) были перечислены 20000руб!", 20000
		end
		
	end
	
end )

